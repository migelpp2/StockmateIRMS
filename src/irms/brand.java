    public void loadBrands() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String sql = "SELECT * FROM brands ORDER BY brand_id";

        try (Connection conn = MySQLConnect.getConnection();
             PreparedStatement pst = conn.prepareStatement(sql);
             ResultSet rs = pst.executeQuery()) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("brand_id"),
                    rs.getString("brand_name")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    public void searchBrands(String keyword) {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);

        String sql = "SELECT * FROM brands WHERE brand_name LIKE ? OR brand_id LIKE ?";

        try (Connection conn = MySQLConnect.getConnection();
             PreparedStatement pst = conn.prepareStatement(sql)) {

            pst.setString(1, "%" + keyword + "%");
            pst.setString(2, "%" + keyword + "%");

            ResultSet rs = pst.executeQuery();

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("brand_id"),
                    rs.getString("brand_name")
                });
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }

    public void autoSearchBrands() {
        txtSearch.getDocument().addDocumentListener(new DocumentListener() {
            public void insertUpdate(DocumentEvent e) { searchNow(); }
            public void removeUpdate(DocumentEvent e) { searchNow(); }
            public void changedUpdate(DocumentEvent e) { searchNow(); }

            private void searchNow() {
                String text = txtSearch.getText().trim();
                if (text.isEmpty()) {
                    loadBrands();
                } else {
                    searchBrands(text);
                }
            }
        });
    }

