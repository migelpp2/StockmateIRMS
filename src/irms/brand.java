    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a brand");
            return;
        }

        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        String name = jTable1.getValueAt(row, 1).toString();

        int confirm = JOptionPane.showConfirmDialog(this,
                "Delete brand: " + name + "?",
                "Confirm",
                JOptionPane.YES_NO_OPTION);

        if (confirm == JOptionPane.YES_OPTION) {
            try (Connection conn = MySQLConnect.getConnection()) {
                String sql = "DELETE FROM brands WHERE brand_id=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setInt(1, id);
                pst.executeUpdate();

                loadBrands();
                JOptionPane.showMessageDialog(this, "Deleted!");

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Select a brand");
            return;
        }

        int id = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
        String current = jTable1.getValueAt(row, 1).toString();

        JTextField txt = new JTextField(current);

        JPanel panel = new JPanel(new GridLayout(0,1));
        panel.add(new JLabel("Brand Name:"));
        panel.add(txt);

        int result = JOptionPane.showConfirmDialog(this, panel, "Edit Brand", JOptionPane.OK_CANCEL_OPTION);

        if (result == JOptionPane.OK_OPTION) {
            String newName = txt.getText().trim();

            if (newName.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Empty field!");
                return;
            }

            try (Connection conn = MySQLConnect.getConnection()) {
                String sql = "UPDATE brands SET brand_name=? WHERE brand_id=?";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, newName);
                pst.setInt(2, id);
                pst.executeUpdate();

                loadBrands();
                JOptionPane.showMessageDialog(this, "Updated!");

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        JTextField txt = new JTextField();

        JPanel panel = new JPanel(new GridLayout(0,1));
        panel.add(new JLabel("Brand Name:"));
        panel.add(txt);

        int result = JOptionPane.showConfirmDialog(this, panel, "Add Brand", JOptionPane.OK_CANCEL_OPTION);

        if (result == JOptionPane.OK_OPTION) {
            String name = txt.getText().trim();

            if (name.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Empty field!");
                return;
            }

            try (Connection conn = MySQLConnect.getConnection()) {
                String sql = "INSERT INTO brands (brand_name) VALUES (?)";
                PreparedStatement pst = conn.prepareStatement(sql);
                pst.setString(1, name);
                pst.executeUpdate();

                loadBrands();
                JOptionPane.showMessageDialog(this, "Added!");

            } catch (SQLException e) {
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

